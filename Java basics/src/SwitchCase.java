
public class SwitchCase {

	public static void main(String[] args) {

		String juice = "orange";

		switch(juice) {

		case "mango":
			System.out.println("you selected mango juice");
			break;
		case "grape":
			System.out.println("you selected grape juice");
			break;
		case "orange":
			System.out.println("you selected orange juice of Rs 120/-");
			break;
		case "apple":
			System.out.println("you selected apple juice");
			break;
		default:
			System.out.println("poyi pani nokada pahaya");

		}

	}
}
